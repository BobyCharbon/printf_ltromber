/* -------------------------------------------------------------------------- */
/*            La fonction bzero() met à 0 (octets contenant « \0 »)           */
/* -------------- les N premiers octets du bloc pointé par STR. ------------- */
/* -------------------------------------------------------------------------- */
/* --------------------------- VALEUR DE RETOUR: / -------------------------- */

#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	ft_memset(str, 0, n);
}

