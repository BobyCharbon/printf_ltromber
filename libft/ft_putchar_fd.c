/* -------------------------------------------------------------------------- */
/*           Écrit le caractère ’c’ sur le file descriptor donné.          */
/* -------------------------------------------------------------------------- */
/* --------------------------- VALEUR DE RETOUR: / -------------------------- */
/* ----------------------- C =  le caractère à écrire ---------------------- */
/* ---------------- FD = le file descriptor sur lequel écrire --------------- */
/* -------------------------------------------------------------------------- */

#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
