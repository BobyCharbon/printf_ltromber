#include "ft_printf.h"

void	ft_print_p(unsigned int p)
{
	char	c;

	c = hexcode(p % 16);
	if (p > 0)
		ft_print_p(p / 16);
	else
	{
		write(1, "0x", 2);
		// write(1, &c, 1);
		return ;
	}
	write(1, &c, 1);
}